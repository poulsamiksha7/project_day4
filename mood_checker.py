mood = input("How are you feeling today? (happy/sad/tired/sick):".lower())

if mood=="happy":
    print("Yay! Keep Smiling 😊")
elif mood=="sad":
    print("Sending virtual hugs🤗")

elif mood=="tired":
    print("Take some rest,you deserve it 💤")

elif mood=="sick":
    print("Drink warm water and rest. Get well soon 🤍")
else:
    print("Hmm... I hope your day gets better 😊")